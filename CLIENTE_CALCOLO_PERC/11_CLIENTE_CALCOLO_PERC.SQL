--CLIENTE_CALCOLO_PERC
SELECT        
	TOP (100) PERCENT MARGINE_COMPLESSIVO.MARGINE_TOT, MARGINE_CLIENTE.COD_CLI, MARGINE_CLIENTE.MARGINE_TOT_CLIENTE, 
    MARGINE_CLIENTE.MARGINE_TOT_CLIENTE / MARGINE_COMPLESSIVO.MARGINE_TOT * 100 AS PERC
FROM            
	MARGINE_COMPLESSIVO CROSS JOIN
    MARGINE_CLIENTE
WHERE        
	(MARGINE_CLIENTE.MARGINE_TOT_CLIENTE / MARGINE_COMPLESSIVO.MARGINE_TOT * 100 >= 0)
ORDER BY PERC DESC